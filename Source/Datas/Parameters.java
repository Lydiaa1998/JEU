package Datas;

/**
 * This class presents all the parameters that are used for the map and the GUI
 */
public class Parameters {
	
	/*
	 * Initial size of the GUI in pixels
	 */
	public static final int WINDOW_WIDTH = 814;
	public static final int WINDOW_HEIGHT = 800;
	
	/*
	 * Height that the HUD takes vertically in the GUI
	 */
	public static final int HUD_HEIGHT = 80;
	public static final int MAP_WIDTH = WINDOW_WIDTH;
	public static final int MAP_HEIGHT = WINDOW_HEIGHT - HUD_HEIGHT;
	
	/*
	 * Amount of squares for the playable grid
	 */
	public static final int GRID_WIDTH = 40;
	public static final int GRID_HEIGHT = 30;
	
}
